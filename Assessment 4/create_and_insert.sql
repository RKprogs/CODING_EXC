
CREATE TABLE WORKER(
WORKER_ID SERIAL PRIMARY KEY,
FIRST_NAME VARCHAR(30),
LAST_NAME VARCHAR(30),
SALARY DECIMAL(10,2),
JOINING_DATE DATE,
DEPARTMENT VARCHAR(40)
);

CREATE TABLE TITLE(
WORKER_REF_ID INTEGER REFERENCES WORKER(WORKER_ID),
WORKER_TITLE VARCHAR(100),
AFFECTED_FROM DATE
);

CREATE TABLE BONUS(
WORKER_REF_ID INTEGER REFERENCES WORKER(WORKER_ID),
BONUS_AMOUNT DECIMAL(10,2),
BONUS_DATE DATE
);

